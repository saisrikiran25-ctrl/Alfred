function isSmartphoneDomainQuestion(text: string) {
  const keywordRegex =
    /(smartphone|mobile|phone|consumer|market|brand|camera|battery|5g|ai|loyalty|purchase|india|flipkart|amazon|samsung|apple|xiaomi|vivo|oppo|oneplus|upgrade|replacement|tam|tpb|theory|constructs|behavior|intention|age|gender|income|loyalty|swot|theories|share|strategy|marketing|retail)/i;
  return keywordRegex.test(text);
}

