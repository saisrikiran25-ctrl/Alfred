export const DOMAIN_LOCK_MESSAGE =
  "I am an intelligence oracle specializing in Consumer Behaviour within the Smartphone Industry based on my verified data. Could you ask something related to that topic?";

function isSmartphoneDomainQuestion(text: string) {
  return /(smartphone|mobile|phone|consumer|market|brand|camera|battery|5g|ai|loyalty|purchase|india|flipkart|amazon|samsung|apple|xiaomi|vivo|oppo|oneplus|upgrade|replacement|tam|tpb|theory|constructs|behavior|intention|age|gender|income|loyalty|swot|theories|share)/i.test(
    text,
  );
}

function calculateScore(
  queryTokens: string[],
  item: (typeof KNOWLEDGE_BASE)[0],
) {
  const searchableText =
    item.title.toLowerCase() +
    " " +
    item.content.toLowerCase() +
    " " +
    item.keywords.toLowerCase();

  let score = 0;
  for (const token of queryTokens) {
    if (token.length > 2) {
      if (searchableText.includes(token)) {
        score += 1;
        // Boost score if it hits the title or keywords
        if (item.title.toLowerCase().includes(token)) score += 5;
        if (item.keywords.toLowerCase().includes(token)) score += 5;
      }
    }
  }
  return score;
}

export function generateSmartBotReply(
  userText: string,
  isDetailed: boolean = false,
) {
  const q = userText.trim().toLowerCase();

  // Basic check for relevant phrasing
  const wordsCount = q.split(" ").length;
  // Let simple greetings or non-related queries trigger lock,
  // but if the user forcefully mentions knowledge base or data, we try.
  if (!isSmartphoneDomainQuestion(q) && !q.includes("data") && wordsCount < 5) {
    return DOMAIN_LOCK_MESSAGE;
  }

  // Tokenization and stopword removal
  const rawTokens = q.split(/[^a-z0-9]+/);
  // Extremely rudimentary stop words
  const stopWords = [
    "what",
    "why",
    "how",
    "when",
    "who",
    "is",
    "are",
    "does",
    "do",
    "the",
    "a",
    "an",
    "in",
    "on",
    "at",
    "to",
    "for",
    "of",
    "and",
    "or",
    "with",
    "about",
    "can",
    "you",
    "my",
    "tell",
    "me",
    "based",
    "from",
    "data",
    "provided",
    "only",
    "knowledge",
    "base",
    "files",
    "information",
    "question",
    "answers",
  ];
  const queryTokens = rawTokens.filter(
    (t) => t.length > 1 && !stopWords.includes(t),
  );

  // If query consists ONLY of stopwords or is empty after filtering
  if (queryTokens.length === 0) {
    return "I can pull data from my exhaustive internal knowledge base. What specific aspect of smartphone consumer behaviour would you like to investigate?";
  }

  // Score all items
  const scoredItems = KNOWLEDGE_BASE.map((item) => ({
    item,
    score: calculateScore(queryTokens, item),
  })).sort((a, b) => b.score - a.score);

  // If no good match, fallback
  if (scoredItems[0].score === 0) {
    return "Based on the provided databanks, I don't have matching specific data for that exact query. My focus is entirely on smartphone consumer behaviour, TAM-TPB integration, purchase factors, brand loyalty, demographics, and market data. Try rephrasing!\n\n💡 You might ask: What are the top purchase factors for Indian smartphone buyers?";
  }

  // Take top 1 or 2 items to form an answer based strictly on the KB
  const topItems = scoredItems.filter((i) => i.score > 0).slice(0, 2);

  let reply = "";
  topItems.forEach((match, index) => {
    reply += "**" + match.item.title + "**\n";
    let content = match.item.content;

    if (isDetailed) {
      // Split content into sentences to make it point-wise
      const sentences = content.split(/(?<=\.)\s+/);
      sentences.forEach((sentence) => {
        if (sentence.trim().length > 0) {
          reply += "- " + sentence.trim() + "\n";
        }
      });
      reply += "\n";
    } else {
      // Concise version - just the first sentence or two
      const sentences = content.split(/(?<=\.)\s+/);
      reply += sentences[0];
      if (sentences.length > 1) {
        reply += " " + sentences[1];
      }
      reply += "\n\n";
    }
  });

  reply +=
    "💡 Ask further: Can you explain more about the impact of " +
    (queryTokens[0] || "this") +
    "?";
  return reply.trim();
}
