function tokenize(text: string): string[] {
  return text
    .toLowerCase()
    .replace(/[^\w\s]/g, "")
    .split(/\s+/)
    .filter((token) => token.length > 2 && !STOP_WORDS.has(token));
}

function calculateAdvancedScore(
  queryTokens: string[],
  item: (typeof KNOWLEDGE_BASE)[0],
) {
  const titleTokens = tokenize(item.title);
  const keywordTokens = tokenize(item.keywords);
  const contentTokens = tokenize(item.content);

  let score = 0;
  const uniqueQueryTokens = new Set(queryTokens);

  uniqueQueryTokens.forEach((token) => {
    // Title matches are extremely valuable
    if (titleTokens.includes(token)) score += 10;
    // Keyword matches are very valuable
    if (keywordTokens.includes(token)) score += 8;
    // Content matches are basic
    if (contentTokens.includes(token)) {
      const freq = contentTokens.filter((t) => t === token).length;
      score += 2 + Math.min(freq, 3); // Frequency boost capped at 3
    }

    // Partial matching for plurals or suffixes (very basic)
    if (item.content.toLowerCase().includes(token)) score += 1;
  });

  return score;
}

function synthesizeTable(content: string): string {
  // Extract patterns like "Samsung (19% share), Xiaomi (18%)" or "Camera quality (82%), Battery (78%)"
  const patterns = content.match(/([A-Za-z\s\/]+)\s*\((\d+%|\d+[^)]+)\)/g);
  if (patterns && patterns.length >= 3) {
    let table = "\n| Metric / Brand | Value / Data |\n| :--- | :--- |\n";
    patterns.forEach((p) => {
      const parts = p.match(/(.+)\((.+)\)/);
      if (parts) {
        table += `| ${parts[1].trim()} | ${parts[2].trim()} |\n`;
      }
    });
    return table + "\n";
  }
  return "";
}

export function generateSmartBotReply(
  userText: string,
  isDetailed: boolean = false,
  history: { role: string; content: string }[] = [],
) {
  const qLower = userText.toLowerCase().trim();
  
  // Early exit for empty or whitespace-only input
  if (!qLower) {
    return "I am scanning my internal knowledge base. Please specify which aspect of the smartphone industry or consumer behaviour you wish to explore (e.g., Apple loyalty, TAM models, or Indian market share).";
  }

  // Handle Context (Simple)
  // If the user says "tell me more" or "why", look at the last assistant message
  let effectiveQuery = qLower;
  if (
    history.length > 0 &&
    (qLower.match(/^(why|how|explain|more|elaborate|tell me more|detail)/) ||
      qLower.length < 10)
  ) {
    const lastAssistantMsg = [...history]
      .reverse()
      .find((m) => m.role === "assistant");
    if (lastAssistantMsg) {
      effectiveQuery += " " + lastAssistantMsg.content.toLowerCase();
    }
  }

  const queryTokens = tokenize(effectiveQuery);

  // If no meaningful tokens after filtering
  if (queryTokens.length === 0) {
    return "I am scanning my internal knowledge base. Please specify which aspect of smartphone industry results or consumer behaviour you wish to explore.";
  }

  // Domain Lock Check - Only if the user actually sent content that isn't a simple 'tell me more'
  if (
    !isSmartphoneDomainQuestion(effectiveQuery) &&
    !effectiveQuery.includes("data") &&
    qLower.length > 5
  ) {
    return DOMAIN_LOCK_MESSAGE;
  }

  // Score and Sort
  const scoredItems = KNOWLEDGE_BASE.map((item) => ({
    item,
    score: calculateAdvancedScore(queryTokens, item),
  })).sort((a, b) => b.score - a.score);

  const topMatch = scoredItems[0];
  if (topMatch.score < 5) {
    return "My analysis of the verified data banks shows no direct correlation for that specific query. However, I can provide insights on **Market Shares**, **Purchase Factors**, **Theoretical Models (TAM/TPB)**, or **Strategic Recommendations**. \n\n💡 Try: 'What are the top purchase factors in India?'";
  }

  // Synthesize Response
  const relevantItems = scoredItems.filter((i) => i.score >= topMatch.score * 0.6).slice(0, 2);
  
  let reply = "";
  
  if (isDetailed) {
    reply += `### Executive Analysis: ${relevantItems[0].item.title}\n\n`;
    
    relevantItems.forEach((match, idx) => {
      if (idx > 0) reply += `---\n\n#### Complementary Insight: ${match.item.title}\n\n`;
      
      const table = synthesizeTable(match.item.content);
      if (table) {
        reply += "#### Statistical Breakdown\n" + table + "\n";
      }

      // Clean the content before bulletizing
      let workingContent = match.item.content;
      
      // 1. Remove markers like "1. ", "2. ", etc globally
      workingContent = workingContent.replace(/\b\d+\.\s+/g, "");
      
      // 2. Remove items that already went into the table to avoid "Double Info"
      const patterns = workingContent.match(/([A-Za-z\s\/]+)\s*\((\d+%|\d+[^)]+)\)/g);
      if (patterns) {
        patterns.forEach(p => {
          workingContent = workingContent.replace(p, "").replace(/,\s*,/g, ",").trim();
        });
      }

      // convert to sentences
      const rawSentences = workingContent.split(/\.\s+/);
      
      rawSentences.forEach(s => {
        let cleanSentence = s.trim().replace(/^,|,$/g, "").trim();
        if (cleanSentence.length < 5) return;

        // Final cleanup of list-like commas
        cleanSentence = cleanSentence.replace(/,(\d+\.)/g, ".");
        
        if (!cleanSentence.match(/[.!?]$/)) cleanSentence += ".";
        reply += `- ${cleanSentence}\n`;
      });
      reply += "\n";
    });

    reply += "#### 🎯 Strategic Implication\n\n";
    reply += "Based on this analysis, stakeholders should prioritize the identified high-impact variables (e.g., " + (queryTokens[0] || "core features") + ") to maximize consumer conversion and brand equity.";
  } else {
    // Concise Mode
    const mainMatch = relevantItems[0].item;
    const rawSentences = mainMatch.content.split(/(?<!\b\d)\.\s+/);
    
    reply += `**${mainMatch.title}**\n\n`;
    reply += rawSentences.slice(0, 2).map(s => s.trim() + (s.trim().endsWith('.') ? '' : '.')).join(" ");
    
    const table = synthesizeTable(mainMatch.content);
    if (table) {
      reply += "\n\n**Quick Stats:**\n" + table.trim();
    }
    
    reply += `\n\n💡 *Pro Tip: Access the 'DETAILED' analysis for full strategic implications.*`;
  }

  return reply.trim();
}
